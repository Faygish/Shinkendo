      <h1>Lidmaatschap</h1>
      <hr>
      <p>De maandelijkse contributie aan Shinkendo Midden Nederland (SMN) bedraagt €25 en dient elke 1e van de maand te worden betaald.</p>
      <br>
<p>Daarnaast is er een jaarlijkse contributie aan de International Shinkendo Federation (ISF) en Kokusai Toyama Ryu Renmei (KTRR). Dit bedraagt €25 voor studenten en militair personeel, €50 euro voor niet-studenten/niet-militair personeel.
</p>
<br>
<p>Als lid van SMN, ISF en KTRR mag je 2 maal per week trainen en geeft dit je het recht mee te doen aan seminars en het afleggen van examens.</p>
