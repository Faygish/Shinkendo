    <li>Tanrengata (solo vormen waarin slagen en technieken worden gecombineerd).</li>
    <li>Tachiuchi (sparren met een partner).</li>
    <li>Tameshigiri (testsnijden van opgerolden tatami-matten met een scherp zwaard).</li>
    </ul>
    </div>
