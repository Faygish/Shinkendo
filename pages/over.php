    <h1> Wat is Shinkendo</h1>
    
    <p>Shinkendo is een zwaardvechtsport waarin je leert om te gaan met het Japanse zwaard, de katana. 
      <br>
       De naam Shinkendo betekent letterlijk ‘De weg van het echte zwaard’. 
      <br>
       Door Shinkendo training zal je jouw kracht, conditie en coördinatievermogen vergroten, waardoor je algehele fysieke en mentale gesteldheid verbeterd wordt.  
       <br>
       Shinkendo is een hereniging van alle belangrijke facetten van het zwaardvechten en de trainingen zijn dan ook opgebouwd uit de 5 centrale aspecten hiervan, de Goho Gorin Gogyo;  
    </p>
